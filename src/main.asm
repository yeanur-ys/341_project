; =============================================================================
; MAIN PROGRAM
; =============================================================================

MAIN PROC

    MOV AX, @DATA
    MOV DS, AX

    ; Initialize the system
    CALL INIT_SYSTEM

    ; Main application loop
MAIN_LOOP:
    ; Display welcome menu
    CALL DISPLAY_MAIN_MENU

    ; Get user choice
    MOV AH, 0Ah
    MOV DX, OFFSET INPUT_BUFFER
    INT 21h

    ; Process menu choice
    MOV AL, [INPUT_BUFFER + 2]
    SUB AL, '0'

    CMP AL, 1
    JE MENU_LOGIN
    CMP AL, 2
    JE MENU_ALLOCATE
    CMP AL, 3
    JE MENU_SEARCH
    CMP AL, 4
    JE MENU_DECOMMISSION
    CMP AL, 5
    JE MENU_HISTORY
    CMP AL, 6
    JE MENU_LOGOUT
    CMP AL, 7
    JE MENU_EXIT

    LEA DX, MSG_INVALID_CHOICE
    CALL PRINT_STRING
    JMP MAIN_LOOP

MENU_LOGIN:
    CALL AUTH_LOGIN
    JMP MAIN_LOOP

MENU_ALLOCATE:
    CALL ALLOCATE_SERVER_NODE
    JMP MAIN_LOOP

MENU_SEARCH:
    CALL SEARCH_NODE
    JMP MAIN_LOOP

MENU_DECOMMISSION:
    CALL DECOMMISSION_NODE
    JMP MAIN_LOOP

MENU_HISTORY:
    CALL DISPLAY_DECOMMISSION_HISTORY
    JMP MAIN_LOOP

MENU_LOGOUT:
    CALL LOGOUT
    JMP MAIN_LOOP

MENU_EXIT:
    LEA DX, MSG_GOODBYE
    CALL PRINT_STRING
    MOV AX, 4C00H
    INT 21H

MAIN ENDP

END MAIN
