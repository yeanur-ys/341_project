PRINT_STRING PROC
    MOV AH, 09h
    INT 21h
    RET
PRINT_STRING ENDP

PRINT_CHAR PROC
    MOV DL, AL
    MOV AH, 02h
    INT 21h
    RET
PRINT_CHAR ENDP

PRINT_NUMBER PROC
    PUSH BX
    PUSH CX
    PUSH DX

    MOV BX, 10
    MOV CX, 0

COUNT_DIGITS:
    XOR DX, DX
    DIV BX
    PUSH DX
    INC CX
    CMP AX, 0
    JNE COUNT_DIGITS

PRINT_DIGITS:
    POP AX
    ADD AL, '0'
    CALL PRINT_CHAR
    LOOP PRINT_DIGITS

    POP DX
    POP CX
    POP BX
    RET
PRINT_NUMBER ENDP

READ_STRING PROC
    MOV AH, 0Ah
    INT 21h
    RET
READ_STRING ENDP

STRING_COMPARE PROC
    PUSH BX
    XOR AX, AX

COMPARE_LOOP:
    MOV AL, [SI]
    MOV BL, [DI]
    CMP AL, BL
    JNE COMPARE_NOT_EQUAL
    CMP AL, 0
    JE COMPARE_EQUAL
    CMP AL, '$'
    JE COMPARE_EQUAL
    INC SI
    INC DI
    JMP COMPARE_LOOP

COMPARE_EQUAL:
    XOR AX, AX
    JMP COMPARE_END

COMPARE_NOT_EQUAL:
    MOV AX, 1

COMPARE_END:
    POP BX
    RET
STRING_COMPARE ENDP

STRING_LENGTH PROC
    PUSH SI
    XOR AX, AX

LENGTH_LOOP:
    MOV BL, [SI]
    CMP BL, 0
    JE LENGTH_END
    CMP BL, '$'
    JE LENGTH_END
    INC AX
    INC SI
    JMP LENGTH_LOOP

LENGTH_END:
    POP SI
    RET
STRING_LENGTH ENDP

STRING_COPY PROC
    PUSH AX

COPY_LOOP:
    MOV AL, [SI]
    MOV [DI], AL
    CMP AL, 0
    JE COPY_END
    CMP AL, '$'
    JE COPY_END
    INC SI
    INC DI
    JMP COPY_LOOP

COPY_END:
    POP AX
    RET
STRING_COPY ENDP

CLEAR_BUFFER PROC
    PUSH AX
    XOR AL, AL

CLEAR_LOOP:
    MOV [DI], AL
    INC DI
    LOOP CLEAR_LOOP

    POP AX
    RET
CLEAR_BUFFER ENDP

FIND_CHAR_IN_STRING PROC
    PUSH AX
    XOR BX, BX

FIND_LOOP:
    MOV CL, [SI]
    CMP CL, AL
    JE CHAR_FOUND
    CMP CL, 0
    JE CHAR_NOT_FOUND
    CMP CL, '$'
    JE CHAR_NOT_FOUND
    INC BX
    INC SI
    JMP FIND_LOOP

CHAR_FOUND:
    POP AX
    RET

CHAR_NOT_FOUND:
    MOV BX, -1
    POP AX
    RET
FIND_CHAR_IN_STRING ENDP

