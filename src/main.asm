READ_DYNAMIC_STRING PROC
    PUSH AX
    PUSH BX
    PUSH DI

    MOV DI, DX
    XOR BX, BX

READ_CHAR_LOOP:
    MOV AH, 01h
    INT 21h

    CMP AL, 0Dh
    JE END_DYNAMIC_STRING

    CMP BX, 59
    JGE READ_CHAR_LOOP

    MOV [DI + BX], AL
    INC BX
    CALL PRINT_CHAR
    JMP READ_CHAR_LOOP

END_DYNAMIC_STRING:
    MOV BYTE [DI + BX], '$'
    MOV BYTE [DI + BX + 1], 0

    POP DI
    POP BX
    POP AX
    RET
READ_DYNAMIC_STRING ENDP

