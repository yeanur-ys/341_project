PUSH_TO_STACK PROC
    PUSH AX
    PUSH BX

    MOV AL, [STACK_PTR]

    CMP AL, 100
    JGE STACK_FULL

    MOV BH, 0
    MOV [DECOMMISSION_STACK + BX], BL

    INC AL
    MOV [STACK_PTR], AL

STACK_FULL:
    POP BX
    POP AX
    RET
PUSH_TO_STACK ENDP

