SEARCH_NODE PROC
    CMP CURRENT_USER_ROLE, 0
    JE NOT_LOGGED_IN_SEARCH

    LEA DX, MSG_SEARCH_MENU
    CALL PRINT_STRING

    LEA DX, MSG_ENTER_SEARCH_QUERY
    CALL PRINT_STRING
    MOV DX, OFFSET SEARCH_INPUT
    CALL READ_STRING

    LEA SI, [SEARCH_INPUT + 2]

    MOV AL, [SI]
    CMP AL, '0'
    JL SEARCH_BY_NAME
    CMP AL, '9'
    JG SEARCH_BY_NAME

    SUB AL, '0'
    CALL SEARCH_BY_NODE_ID
    JMP SEARCH_DISPLAY_HEALTH

SEARCH_BY_NAME:
    CALL SEARCH_BY_COMPANY_NAME

SEARCH_DISPLAY_HEALTH:
    CALL DISPLAY_RACK_HEALTH
    RET

NOT_LOGGED_IN_SEARCH:
    LEA DX, MSG_NOT_LOGGED_IN
    CALL PRINT_STRING
    RET

SEARCH_NODE ENDP

SEARCH_BY_NODE_ID PROC
    PUSH BX
    PUSH CX

    CMP AL, 0
    JL SEARCH_ID_INVALID
    CMP AL, 9
    JG SEARCH_ID_INVALID

    XOR AH, AH
    MOV BX, AX

    LEA DX, MSG_NODE_STATUS
    CALL PRINT_STRING
    MOV AL, BL
    ADD AL, '0'
    CALL PRINT_CHAR
    LEA DX, MSG_NEWLINE
    CALL PRINT_STRING

    MOV AL, [SERVER_NODES + BX]
    CMP AL, STATUS_ONLINE
    JE DISPLAY_RUNNING

    LEA DX, MSG_NODE_OFFLINE
    CALL PRINT_STRING
    JMP SEARCH_ID_END

DISPLAY_RUNNING:
    LEA DX, MSG_NODE_RUNNING
    CALL PRINT_STRING

    MOV AX, BX
    MOV CX, 60
    MUL CX
    MOV SI, AX
    ADD SI, OFFSET SERVER_COMPANY_NAMES
    CALL DISPLAY_STRING_AT_SI
    LEA DX, MSG_NEWLINE
    CALL PRINT_STRING

    JMP SEARCH_ID_END

SEARCH_ID_INVALID:
    LEA DX, MSG_NODE_NOT_FOUND
    CALL PRINT_STRING

SEARCH_ID_END:
    POP CX
    POP BX
    RET
SEARCH_BY_NODE_ID ENDP

SEARCH_BY_COMPANY_NAME PROC
    PUSH AX
    PUSH BP
    PUSH BX
    PUSH CX
    PUSH SI
    PUSH DI

    MOV BX, 0
    MOV CX, 10
    MOV BP, 0

SEARCH_COMPANY_LOOP:
    PUSH SI
    
    MOV DL, [SERVER_NODES + BX]
    CMP DL, STATUS_ONLINE
    JNE SEARCH_COMPANY_NEXT

    MOV AX, BX
    MOV CX, 60
    MUL CX
    MOV DI, AX
    ADD DI, OFFSET SERVER_COMPANY_NAMES

    CALL COMPARE_COMPANY_NAMES
    CMP AX, 0
    JNE SEARCH_COMPANY_NEXT

    LEA DX, MSG_NODE_STATUS
    CALL PRINT_STRING
    MOV AL, BL
    ADD AL, '0'
    CALL PRINT_CHAR
    LEA DX, MSG_NEWLINE
    CALL PRINT_STRING

    LEA DX, MSG_NODE_RUNNING
    CALL PRINT_STRING

    ; Set a variable or register to mark found
    MOV BP, 1

SEARCH_COMPANY_NEXT:
    POP SI
    INC BX
    CMP BX, 10
    JGE SEARCH_COMPANY_END
    JMP SEARCH_COMPANY_LOOP

SEARCH_COMPANY_END:
    CMP BP, 1
    JE SEARCH_CO_FOUND
    LEA DX, MSG_NODE_NOT_FOUND
    CALL PRINT_STRING

SEARCH_CO_FOUND:
    POP DI
    POP SI
    POP CX
    POP BX
    POP BP    ; Need to properly manage Stack if using BP
    POP AX
    RET
SEARCH_BY_COMPANY_NAME ENDP

COMPARE_COMPANY_NAMES PROC
    PUSH BX

COMPARE_CO_LOOP:
    MOV AL, [SI]
    MOV BL, [DI]

    CMP AL, 'a'
    JL COMPARE_CO_CHECK_SECOND
    CMP AL, 'z'
    JG COMPARE_CO_CHECK_SECOND
    SUB AL, 32

COMPARE_CO_CHECK_SECOND:
    CMP BL, 'a'
    JL COMPARE_CO_DO_COMPARE
    CMP BL, 'z'
    JG COMPARE_CO_DO_COMPARE
    SUB BL, 32

COMPARE_CO_DO_COMPARE:
    CMP AL, BL
    JNE COMPARE_CO_NOT_EQUAL

    CMP AL, 0
    JE COMPARE_CO_EQUAL
    CMP AL, '$'
    JE COMPARE_CO_EQUAL
    CMP BL, 0
    JE COMPARE_CO_EQUAL
    CMP BL, '$'
    JE COMPARE_CO_EQUAL

    INC SI
    INC DI
    JMP COMPARE_CO_LOOP

COMPARE_CO_EQUAL:
    XOR AX, AX
    JMP COMPARE_CO_END

COMPARE_CO_NOT_EQUAL:
    MOV AX, 1

COMPARE_CO_END:
    POP BX
    RET
COMPARE_COMPANY_NAMES ENDP

DISPLAY_RACK_HEALTH PROC
    LEA DX, MSG_RACK_HEALTH
    CALL PRINT_STRING

    CALL COUNT_ACTIVE_NODES
    XOR AH, AH
    CALL PRINT_NUMBER

    LEA DX, MSG_ACTIVE_NODES
    CALL PRINT_STRING

    CALL COUNT_AVAILABLE_NODES
    XOR AH, AH
    CALL PRINT_NUMBER

    LEA DX, MSG_AVAILABLE_NODES
    CALL PRINT_STRING

    RET
DISPLAY_RACK_HEALTH ENDP

GET_NODE_STATISTICS PROC
    PUSH BX
    PUSH CX

    XOR AL, AL
    XOR AH, AH
    MOV BX, 0
    MOV CX, 10

STATS_LOOP:
    MOV DL, [SERVER_NODES + BX]
    CMP DL, STATUS_ONLINE
    JNE STATS_OFFLINE
    INC AL
    JMP STATS_NEXT

STATS_OFFLINE:
    INC AH

STATS_NEXT:
    INC BX
    LOOP STATS_LOOP

    POP CX
    POP BX
    RET
GET_NODE_STATISTICS ENDP

